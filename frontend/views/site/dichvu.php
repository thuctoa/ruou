                <h3 class="text-center">LỊCH SỬ HÌNH THÀNH VÀ PHÁT TRIỂN 
                    TRUNG TÂM THIẾT KẾ, CHẾ TẠO VÀ CHUYỂN GIAO THIẾT BỊ CÔNG NGHỆ CAO VIỆT NAM</h3>

                <p>Trung tâm Thiết kế, chế tạo và chuyển giao Máy Thiết bị Công nghệ Trí Việt đã được ra đời và phát triển gắn liền với các hoạt động giảng dạy, nghiên cứu khoa học và chuyển giao thiết bị công nghệ của GVC.ThS. Nguyễn Văn Quang và các cộng sự. Trong hoạt động giảng dạy “thầy Quang” đã “truyền được cảm hứng” đến các họa trò của mình, trong hoạt động nghiên cứu khoa học “thầy Quang” đã có nhiều “đột phá” trong các đề tài, dự án từ cấp cơ sở, cấp tỉnh đến cấp Bộ trong các lĩnh vực là định hướng nghiên cứu khoa học chính của mình như: sản xuất, chưng cất rượu, tinh dầu, chiết xuất dược liệu, sấy nông sản và dược liệu, xử lý khí thải, thu hồi nhiệt thải, mô hình hóa các quá trình công nghệ, ... Năm 2014, “thầy Quang” đã đạt giải nhì Hội nghị Khoa học trẻ toàn quốc các trường Đại học và Cao đẳng khối Nông - Lâm - Ngư - Thủy lần thứ VI. Các hoạt động giảng dạy và nghiên cứu khoa học của “thầy Quang” là tiền đề để thầy phát triển các hoạt động chuyển giao thiết bị công nghệ trong nước với nhiều “thành tích ấn tượng” mà trong các trang chuyên mục về các sản phẩm thầy đã nói.</p>
                <p>Câu chuyện về cuộc đời “thầy Quang” là một chuỗi dài “gian khó” với nhiều “biến cố” trong cuộc đời để đưa thầy đến với “nghiệp” và “nghề”. Cái “nghiệp” là một “duyên nợ” của cuộc đời thầy! Thầy đã từng bước lên bụt giảng của trường Đại học Nông Lâm Bắc Giang khi mới tốt nghiệp Đại học Bách Khoa Hà Nội năm 2003 đã để lại rất nhiều tình cảm cho thầy và trò của Nhà trường. Nhưng rồi thầy lại phải rời bỏ bụt giảng để làm các công trình thiết bị công nghệ trong và ngoài nước. Năm 2009, thầy lại trở lại bụt giảng của trường Đại học Nông - Lâm Bắc Giang trong nỗi niềm hân hoan của thầy và trò Nhà trường. Với những trải nghiệm thực tiễn trong việc triển khai thiết bị công nghệ thầy đã đâò tạo được rất nhiều học trò có cả “chữ” và “người”. Đó là cái “nghiệp” của thầy! Cái “nghề” đến với thầy như một “định mệnh” của cuộc đời thầy! Thầy “không có sự chọn lựa”, thầy “phải làm, phải thành công”, đó là “con đường duy nhất” để thầy có thể vượt qua được những “bước ngoặc” của cuộc đời. Thầy cứ “từng bước”, ‘từng bước” vượt qua hết những chặng đường “gian khó” và những ”biến cố” trong cuộc đời đi từ “thành công này” đến “thành công khác”, “bản lĩnh”, “kinh nghiệm”, “kiến thức” của thầy được tôi luyện nhiều hơn qua mỗi công trình thiết bị công nghệ! Đó là cài “nghề” của thầy!</p>
                <p>Những năm tháng “trăn trở” với “nghiệp” và “lăn lộn” với “nghề” thầy đã có được rất nhiều người thân, bạn bè, đồng nghiệp, học trò, những người thợ lành nghề và những cộng sự “chân tình”, “đắc lực” giúp đỡ, tiếp sức cho những “chặng đường lịch sử” của thầy! Đó là tài sản lớn nhất của cuộc đời thầy, nó là động lực để thầy vươn đến những “mục tiêu” lớn hơn, xa hơn trong cuộc đời thầy.</p>
                <p>Sự ra đời của Trung tâm Thiết kế, chế tạo và chuyển giao Máy Thiết bị Công nghệ Trí Việt là sự “kết tinh” của “trí”, “lực” và “tâm” của thầy, là sự “hội tụ” của nhiều con người có cùng “một cách nghĩ”, “môt cách làm”, “một cái đích”, “một niềm đam mê”, “một tình yêu” trong cuộc đời này!</p> 

